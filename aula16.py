"""
alteradores aritmeticos
+, -, *, /, //, **, %, ()
"""
print('multiplicação * ', 10 * 10)
print('multiplicação * ', 10 * 'andre')  # pode multiplicar um número int por uma str, assim multi a str pelo número int
print('adição +', 10 + 10)
print('adição +', '5' + '5')  # junta o que tem dentro das aspas, colocando um ao lado do outro
print('subtração -', 10 - 5)
print('divisão /', 10 / 2)
print('divisão /', 10.5 / 3)
print(2 ** 10)  # potênciação
print(10 % 3)  # o sinal de % vai me dar o resto (o que sobrar) da conta
print(10+2*3)  # normalmente vai primeiro fazer a multi depois a soma
print((10+2)*3)  # primeiro o que está entre () e depois segue a sequência normal
